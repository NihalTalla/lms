class Main {
  public static void main() {
    int a = 5;
    int b = 3;

    System.out.println(a & b);   // 1
    System.out.println(a | b);   // 7
    System.out.println(a ^ b);   // 6
    System.out.println(a << 1);  // 10
    System.out.println(a >> 1);  // 2
    System.out.println(~a);      // -6
  }
}
